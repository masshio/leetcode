/**
 * @param {number[]} nums
 */
var NumArray = function(nums) {
  this.numArr = nums
};

/** 
 * @param {number} left 
 * @param {number} right
 * @return {number}
 */
NumArray.prototype.sumRange = function(left, right) {
  return this.numArr.slice(left, right+1).reduce((a, b) => a+b, 0)
};


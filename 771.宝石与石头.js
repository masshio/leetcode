/**
 * @param {string} jewels
 * @param {string} stones
 * @return {number}
 */
var numJewelsInStones = function(jewels, stones) {
  const reg = new RegExp(`[^${jewels}]`, 'g')
  return stones.replace(reg, '').length
};
